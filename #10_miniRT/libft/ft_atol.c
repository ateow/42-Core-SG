#include "libft.h"

long	ft_atol(const char *nb)
{
	long	res;
	int		sign;

	res = 0;
	sign = 1;
	while (*nb == '\t' || *nb == '\n' || *nb == '\v'
		|| *nb == '\f' || *nb == '\r' || *nb == ' ')
		nb++;
	if (*nb == '-' || *nb == '+')
	{
		if (*nb == '-')
			sign = -1;
		nb++;
	}
	while ('0' <= *nb && *nb <= '9')
	{
		res = res * 10 + (*nb - '0');
		nb++;
	}
	return (res * sign);
}
